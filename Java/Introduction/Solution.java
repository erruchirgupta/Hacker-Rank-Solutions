import java.util.*;

public class Solution {

    public static void main(String[] args) {
        
        Scanner scan = new Scanner(System.in);
        int ar[] = new int[3];
         
        for(int i=0; i < ar.length; i++){
            ar[i] = scan.nextInt();
        }
        
        for(int i=0; i < ar.length; i++){
            System.out.println(ar[i]);
        }
        
    }
}
